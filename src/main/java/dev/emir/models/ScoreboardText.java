package dev.emir.models;

public class ScoreboardText {
    private String left;
    private String right;

    public ScoreboardText(String left, String right) {
        this.left = left;
        this.right = right;
    }

    public String getLeft() {
        return this.left;
    }

    public String getRight() {
        return this.right;
    }
}
